# Arithmetic Operations
a = int(input("Enter the value of a :"))
b = int(input("Enter the value of b :"))

# Sum
print(f"The sum of a and b is {a+b}")
# Difference
print(f"The sum of a and b is {abs(a-b)}")
# Product
print(f"The sum of a and b is {a*b}")
# Division
print(f"The sum of a and b is {a/b}")
# Modulus
print(f"The sum of a and b is {a%b}")