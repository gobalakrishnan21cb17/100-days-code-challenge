# String Operations
msg = "Python Programming"

#Indexing a String
print(msg[0])
#Negative Indexing
print(msg[-2]) 
#Slicing a String
print(msg[0:3])
#Striding a String
print(msg[2:5:2])
#Concantenation of Strings
print("I love",msg)
#Uppercase - Changes the value to Uppercase
print(msg.upper())
#Lowercase - Changes the value to Lowercase
print(msg.lower())
#Swapcase - Swaps the Lower and Uppercase
print(msg.swapcase())
#CaseFold - Returns the lowercase of the data
print(msg.casefold())